/// A class that contains API endpoint constants.
class ApiEndpoints {
  /// The endpoint for GET requests.
  static const String getEndpoint = '/get_endpoint';

  /// The endpoint for POST requests.
  static const String postEndpoint = '/post_endpoint';

  /// The endpoint for PUT requests.
  static const String putEndpoint = '/put_endpoint';

  /// The endpoint for DELETE requests.
  static const String deleteEndpoint = '/delete_endpoint';

  /// The endpoint for PATCH requests.
  static const String patchEndpoint = '/patch_endpoint';

  /// The endpoint for multipart POST requests.
  static const String multipartPostEndpoint = '/multipart_post_endpoint';
}
